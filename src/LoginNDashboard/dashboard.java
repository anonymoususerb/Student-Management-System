    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
         // Get selected row index
    int selectedRow = stuTable.getSelectedRow();
    
    if (selectedRow == -1) {
        JOptionPane.showMessageDialog(this, "Please select a student to update.", "No Selection", JOptionPane.WARNING_MESSAGE);
        return;
    }

    // Get updated values from input fields
    String id = stuID.getText().trim();
    String firstName = stuFirstname.getText().trim();
    String lastName = stuLastname.getText().trim();
    String middleName = stuMiddlename.getText().trim();
    String course = (String) dropCourse.getSelectedItem();
    String yearLevel = (String) dropYear.getSelectedItem();
    String status = (String) dropStatus.getSelectedItem();

    // Validate inputs
    if (id.isEmpty() || firstName.isEmpty() || lastName.isEmpty() || course == null || yearLevel == null || status == null) {
        JOptionPane.showMessageDialog(this, "Please fill in all required fields!", "Input Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    // Format full name as "Last, First Middle"
    String fullName = lastName + ", " + firstName + " " + middleName;

    // Update the ArrayList (students)
    Student updatedStudent = new Student(id, firstName, lastName, middleName, course, yearLevel, status);
    students.set(selectedRow, updatedStudent);

    // Update JTable model
    DefaultTableModel model = (DefaultTableModel) stuTable.getModel();
    model.setValueAt(id, selectedRow, 0);
    model.setValueAt(fullName, selectedRow, 1);
    model.setValueAt(course, selectedRow, 2);
    model.setValueAt(yearLevel, selectedRow, 3);
    model.setValueAt(status, selectedRow, 4);

    // Show confirmation message
    JOptionPane.showMessageDialog(this, "Student data updated successfully!", "Success", JOptionPane.INFORMATION_MESSAGE);

    // Clear input fields
    stuID.setText("");
    stuFirstname.setText("");
    stuLastname.setText("");
    stuMiddlename.setText("");
    dropCourse.setSelectedIndex(0);
    dropYear.setSelectedIndex(0);
    dropStatus.setSelectedIndex(0);
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // Get selected row index
    int selectedRow = stuTable.getSelectedRow();

    if (selectedRow == -1) {
        JOptionPane.showMessageDialog(this, "Please select a student to delete.", "No Selection", JOptionPane.WARNING_MESSAGE);
        return;
    }

    // Confirm before deleting
    int confirm = JOptionPane.showConfirmDialog(this, "Are you sure you want to delete this student?", "Confirm Deletion", JOptionPane.YES_NO_OPTION);
    
    if (confirm == JOptionPane.YES_OPTION) {
        // Remove from ArrayList
        students.remove(selectedRow);

        // Remove from JTable model
        DefaultTableModel model = (DefaultTableModel) stuTable.getModel();
        model.removeRow(selectedRow);

        // Show confirmation message
        JOptionPane.showMessageDialog(this, "Student deleted successfully!", "Success", JOptionPane.INFORMATION_MESSAGE);
    }
    }//GEN-LAST:event_btnDeleteActionPerformed

