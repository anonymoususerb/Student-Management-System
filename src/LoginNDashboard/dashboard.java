    private void txtSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtSearchActionPerformed
        // Get the search query
    String searchQuery = txtSearch.getText().trim().toLowerCase();

    // Get the table model
    DefaultTableModel model = (DefaultTableModel) stuTable.getModel();
    
    // Clear existing rows in the table
    model.setRowCount(0);

    // Loop through the students list and find matches
    for (Student student : students) {
        String id = student.getId().toLowerCase();
        String fullName = (student.getLastName() + ", " + student.getFirstName() + " " + student.getMiddleName()).toLowerCase();
        String course = student.getCourse().toLowerCase();
        String yearLevel = student.getYearLevel().toLowerCase();
        String status = student.getStatus().toLowerCase();

        // Check if the search query matches any field
        if (id.contains(searchQuery) || fullName.contains(searchQuery) || course.contains(searchQuery) || yearLevel.contains(searchQuery) || status.contains(searchQuery)) {
            model.addRow(new Object[]{id, fullName, course, yearLevel, status});
        }
    }
    }//GEN-LAST:event_txtSearchActionPerformed

